function createPlayerCard(player) {
    var baseUrl = "https://kr.callawaygolf.com/team-callaway/";
    if (player.league === "KPGA") {
        baseUrl += "KPGA/"; 
    } else if (player.league === "KLPGA") {
        baseUrl += "KLPGA/"; 
    }
    else if (player.league === "PGA") {
        baseUrl += "PGA/"; // PGA 異붽�
    }
    

   
    var playerUrl = baseUrl + player.identifier; // �꾩껜 URL 議고빀

    var container = document.createElement('div');
    container.className = 'product-lister-item col-md-3 col-sm-6 col-xs-6 clearfix tour_player_item';
    var imageContent = document.createElement('div');
    imageContent.className = 'product-grid-image_content tour_player_image';
    var link = document.createElement('a');
    link.href = playerUrl; 
    var image = document.createElement('img');
    image.src = player.imgUrl;
    image.alt = player.name;
    image.title = player.name;
    image.loading = "lazy";

    link.appendChild(image);
    imageContent.appendChild(link);
    container.appendChild(imageContent);

    var nameContainer = document.createElement('div');
    nameContainer.className = 'tour_player';
    var name = document.createElement('h3');
    name.className = 'tour_playerName';
    name.textContent = player.name;

    nameContainer.appendChild(name);
    container.appendChild(nameContainer);

    return container;
}

